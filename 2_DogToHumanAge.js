// DOG TO HUMAN AGE SOLUTION
// This is intended to make best practice on making 
// mathematical procedure clear while programming

// dog to human age solution is given by:
// humanAge   = (dogAge - 2)x4 + 21.

// The formula above has some errors, the programm may or not fix them and 
// calculate as written. so to ensure that doesn't happen you correct the formula from the start in your code.

var dogAge = prompt("What is your dogs' age? ");

var humanAge = ((dogAge-2)*4)+21;
alert("Your dogs age is equivalent to "+humanAge+" years of human.")



